package model;

public class Proprietario {

	private String proprietario;
	private String responsavel;
	private String cia_orgao;
	private String depto_secao;
	private String logradouro;
	private String criacao;
	public String getProprietario() {
		return proprietario;
	}
	public void setProprietario(String proprietario) {
		this.proprietario = proprietario;
	}
	public String getResponsavel() {
		return responsavel;
	}
	public void setResponsavel(String responsavel) {
		this.responsavel = responsavel;
	}
	public String getCia_orgao() {
		return cia_orgao;
	}
	public void setCia_orgao(String cia_orgao) {
		this.cia_orgao = cia_orgao;
	}
	public String getDepto_secao() {
		return depto_secao;
	}
	public void setDepto_secao(String depto_secao) {
		this.depto_secao = depto_secao;
	}
	public String getLogradouro() {
		return logradouro;
	}
	public void setLogradouro(String logradouro) {
		this.logradouro = logradouro;
	}
	public String getCriacao() {
		return criacao;
	}
	public void setCriacao(String criacao) {
		this.criacao = criacao;
	}

}
