package model;

public class Longitude {

	private double grau;
	private double mininuto;
	private double segundo;
	private String direcao;

	public double getGrau() {
		return grau;
	}

	public void setGrau(double grau) {
		this.grau = grau;
	}

	public double getMininuto() {
		return mininuto;
	}

	public void setMininuto(double mininuto) {
		this.mininuto = mininuto;
	}

	public double getSegundo() {
		return segundo;
	}

	public void setSegundo(double segundo) {
		this.segundo = segundo;
	}

	public String getDirecao() {
		return direcao;
	}

	public void setDirecao(String direcao) {
		this.direcao = direcao;
	}

}
